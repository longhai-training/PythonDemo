# 在Python中，可对整数执行加（+ ）减（- ）乘（* ）除（/ ）运算

print(2 + 3)
print(2 - 3)
print(2 * 3)
print(2 / 3)


# Python将带小数点的数字都称为浮点数,但需要注意的是,结果包含的小数位数可能是不确定的
print(0.2 + 0.3)
print(0.2 - 0.3)## = -0.09999999999999998
print(0.2 * 0.3)
print(0.2 / 0.3)

